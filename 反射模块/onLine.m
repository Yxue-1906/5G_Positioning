%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%函数说明:
%判断给定的点是否在两个点连成的直线上
%
%Input:
%   x0,y0,x1,y1,x2,y2
%   第一个坐标为需要判断的点坐标
%
%
%Output:是则输出1,否则输出0
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function onLine=onLine(x0,y0,x1,y1,x2,y2)
    onLine=(x0-x1)*(y0-y2)==(x0-x2)*(y0-y1);

end