function P=assist(x3,y3,x1,y1,x2,y2)
    %(x1,y1)，（x2，y2）为线段端点，（x3，y3）为已知点
    V = zeros(size(x1,1),2);
    V(:,1) = ((x2-x1).*(y3-y1)-(y2-y1).*(x3-x1))./((x2-x1).^2+(y2-y1).^2).*(y2-y1);
    V(:,2) = ((x2-x1).*(y3-y1)-(y2-y1).*(x3-x1))./((x2-x1).^2+(y2-y1).^2).*-(x2-x1);
    % P是对称点
    P = zeros(size(V,1),2);
    P(:,1) = x3 + 2.*V(:,1);
    P(:,2) = y3 + 2.*V(:,2);
end