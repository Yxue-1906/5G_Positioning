%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%函数说明:
%此函数用于获取该纬度的地球半径
%
%Input:纬度
%Output;该纬度地球半径
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function  radius=getRadius(LAT)
%用来获取该纬度的半径
Ea = 6378137;
Eb = 6356725;
ec = Eb + (Ea - Eb) * (90.0 -LAT) / 90.0;%从赤道到极地地球半径修正值
radius = ec * cos (LAT * pi / 180.0) ;%所在纬度的半径值
end

